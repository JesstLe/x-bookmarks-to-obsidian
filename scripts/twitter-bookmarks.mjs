                const date = new Date(bookmark.timestamp).toISOString().split('T')[0];
                const safeHandle = bookmark.handle.replace(/[^a-zA-Z0-9]/g, '_');
                const safeTitle = bookmark.text.substring(0, 30).replace(/[^\w\s]/g, '').trim();
